    /// No thumb has been clicked.
    /// </summary>
    None,

    /// <summary>
    /// The thumb up has been clicked.
    /// </summary>
    Liked,

    /// <summary>
    /// The thumb down has been clicked.
    /// </summary>
    Disliked,
}
